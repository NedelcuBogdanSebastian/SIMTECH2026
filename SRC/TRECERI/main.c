volatile uint32_t *DWT_CONTROL = (volatile uint32_t *)0xE0001000;
volatile uint32_t *DWT_CYCCNT  = (volatile uint32_t *)0xE0001004;
volatile uint32_t *SCB_DEMCR   = (volatile uint32_t *)0xE000EDFC;

void DWT_Enable (void) {
    // Enable the use of DWT
    *SCB_DEMCR = *SCB_DEMCR | 0x01000000;
    // Reset the counter
    *DWT_CYCCNT = 0;
    // Enable cycle counter
    *DWT_CONTROL = *DWT_CONTROL | 1;
}

void DWT_Delay (uint32_t us) {  // Microseconds
    uint32_t delayTicks = us * (SystemCoreClock / 1000000L);
    uint32_t startTick = *DWT_CYCCNT;
    while (*DWT_CYCCNT - startTick < delayTicks);
}

#define PI        3.1415926535897932384626433832795
#define DEG2RAD   0.01745329251994329576923690768489
#define RAD2DEG   57.295779513082320876798154814105  // 180/PI
#define EPSILON   1e-8 // Small value for zero comparison

#define ADC_SCALE  (2.39f / (32767.0f * 3.0f))

// ============================================================================
//  Goertzel precomputed constants for bin k=9, N=2048
//  omega = 2 * PI * 9 / 2048 = 0.027613568...
//  These avoid runtime cosf/sinf calls.
// ============================================================================
#define GOERTZEL_COSW    0.99961882f   // cosf(2 * PI * 9 / 2048)
#define GOERTZEL_SINW    0.02761291f   // sinf(2 * PI * 9 / 2048)
#define GOERTZEL_COEFF   1.99923764f   // 2.0f * GOERTZEL_COSW

// Extract phase in degrees from Goertzel accumulator state (s1, s2).
// Returns phase in [0, 360) with calibration offset applied,
// or 0.0f if the signal is below noise floor.
float goertzel_phase(float s1, float s2) {
    // Extract real and imaginary parts of DFT bin k
    float real_part = s1 - s2 * GOERTZEL_COSW;
    float imag_part = s2 * GOERTZEL_SINW;

    // Guard against zero vector (undefined phase)
    if (fabsf(real_part) < EPSILON && fabsf(imag_part) < EPSILON) {
        return 0.0f;
    }

    // Compute phase in degrees using float-precision atan2
    float angle_deg = atan2f(imag_part, real_part) * (float)RAD2DEG;

    // Robust normalisation to [0, 360)
    angle_deg = fmodf(angle_deg, 360.0f);
    if (angle_deg < 0.0f) {
        angle_deg += 360.0f;
    }

    // Non-finite guard
    if (isnan(angle_deg) || isinf(angle_deg)) {
        return 0.0f;
    }

    return angle_deg;
}

// ============================================================================
//  process_channel: read SRAM, window, Goertzel, RMS - all in one pass
// ============================================================================
//
// This replaces the old 3-step sequence per channel:
//   1. load_Channel_To_FFTbuffer()  - read SRAM into 16 KB xyData[] buffer
//   2. real_fft(xyData, 2048)       - O(N log N) FFT with double twiddles
//   3. myfftPhase(xyData, 2048, 9)  - extract phase from bin 9
//
// Now done in one O(N) streaming pass with only 3 float accumulators
// (s0, s1, s2) and no large buffer needed at all.
//
// Input:  channel  - ADC channel index (0..5)
// Output: *phase   - phase in degrees [0, 360) at bin 9 (~50 Hz)
// Return: RMS voltage = (Vmax - Vmin) * 0.353
float process_channel(uint8_t channel, float *phase) {
    uint16_t n;

    // A.
    // Compute RMS using minCH/maxCH = (maxCH - minCH) * 0.353f
    float minCH = 1000.0f;
    float maxCH = -1000.0f;

    // B.
    // Accumulate the sum of squares for entire signal
    // This mitigate transients problem, where one transient
    // break minCH/maxCH tracking if appear (single sample too high will rise the
    // float rms = (maxCH - minCH) * 0.353f result)
    float sum_sq = 0.0f;

    // Goertzel accumulator state
    float s0 = 0.0f, s1 = 0.0f, s2 = 0.0f;

    // Start SRAM streaming read from address zero
    ENABLE_RAM;
    SPISend(READ);
    SPISend(0x00);  // Address MSB
    SPISend(0x00);  // Address LSB

    for (n = 0; n < 2048; n++) {
        // Read all 6 channels at once from SRAM (12 bytes per sample)
        MSB0 = SPISend(0xFF); LSB0 = SPISend(0xFF);
        MSB1 = SPISend(0xFF); LSB1 = SPISend(0xFF);
        MSB2 = SPISend(0xFF); LSB2 = SPISend(0xFF);
        MSB3 = SPISend(0xFF); LSB3 = SPISend(0xFF);
        MSB4 = SPISend(0xFF); LSB4 = SPISend(0xFF);
        MSB5 = SPISend(0xFF); LSB5 = SPISend(0xFF);

        // Select the requested channel (reuse MSB0/LSB0 to save memory)
        switch (channel) {
            // case 0: MSB0/LSB0 already contain CH0 data
            case 1: MSB0 = MSB1; LSB0 = LSB1; break;
            case 2: MSB0 = MSB2; LSB0 = LSB2; break;
            case 3: MSB0 = MSB3; LSB0 = LSB3; break;
            case 4: MSB0 = MSB4; LSB0 = LSB4; break;
            case 5: MSB0 = MSB5; LSB0 = LSB5; break;
            default: break;
        }

        // Convert 16-bit ADC value to voltage
        float raw = (float)((int16_t)((MSB0 << 8) | LSB0)) * ADC_SCALE;

        // A.
        // Track min/max for RMS calculation
        if (raw > maxCH) maxCH = raw;
        if (raw < minCH) minCH = raw;

        // B.
        //sum_sq += raw * raw;

        // Apply Flat-Top window and feed into Goertzel accumulator
        // This is the core of the Goertzel recurrence:
        //   s0 = windowed_sample + coeff * s1 - s2
        float windowed = raw * flattop_window[n];
        s0 = windowed + GOERTZEL_COEFF * s1 - s2;
        s2 = s1;
        s1 = s0;
    }
    DISABLE_RAM;

    // A.
    // Compute RMS voltage for this channel
    float rms = (maxCH - minCH) * 0.353f;

    // B.
    // RMS is computed to be wideband (all harmonics) and only the phase to be narrowband (50 Hz only) !!!
    //float rms = sqrtf(sum_sq / 2048.0f);   // true RMS

    // Extract phase from Goertzel state (only if signal is above noise floor)
    if (rms > 0.015f) {  // Equivalent to 2.678 mA on 5.6 ohm resistor
        *phase = goertzel_phase(s1, s2);
    } else {
        *phase = 0.0f;
    }

    return rms;
}

// Adjust the phase to be an integer with `2 decimals` ( * 100)
uint16_t adjust_phase(float phase, float phase_difference)
{
    // -- Guard: DWT counter overflow / hardware fault ----------------------
    if (phase_difference >= 359.0f) {
        return 0;
    }

    // -- Guard: non-finite inputs ------------------------------------------
    if (isnan(phase) || isinf(phase) ||
        isnan(phase_difference) || isinf(phase_difference)) {
        return 0;
    }

    // Register 41 stores the trim offset in units of 0.01 deg
    // float trim_deg = (float)(int16_t)readHoldingRegister(41) / 100.0f;
    float trim_deg = 0;

    // -- Apply corrections ------------------------------------------------
    float adjusted_phase = phase - phase_difference - trim_deg;

    // -- Robust normalisation to [0, 360) ---------------------------------
    adjusted_phase = fmodf(adjusted_phase, 360.0f);
    if (adjusted_phase < 0.0f) {
        adjusted_phase += 360.0f;
    }

    // -- Scale and clamp before uint16_t cast -----------------------------
    float scaled = adjusted_phase * 100.0f;
    if (scaled >= 36000.0f) scaled = 35999.0f;   // clamp: 359.99 deg

    return (uint16_t)scaled;
}


// Adjust the voltage so that we have current only in 0..100 mA
uint16_t adjust_voltage (float voltage) {
    uint16_t voltage_value = 0;

    if (isnan(voltage) || isinf(voltage)) return 0;

    // Prevent negative values
    if (voltage < 0)
        return 0;

    // Scale by 10000 and convert to integer
    // The ADC 'sees' voltage up to 0.6V without degraded precision
    voltage_value = (uint16_t)(voltage * 10000.0f);

    // Top voltage at 100 mA (0.1 A * 5.6 OHM = 0.56 V * 10000.0 = 5600)
    if (voltage_value > 5600)
        return 5600;

    return voltage_value;
}

void HEARTBEAT(void) {
    GPIOA->BRR = GPIO_Pin_8;   // Pull heart beat pin down for 500 us
    DWT_Delay(500);
    GPIOA->BSRR = GPIO_Pin_8;  // Release heart beat pin
}

