/*******************************************************************
 *
 * Non-blocking wait for zero cross LO to HI signal transition.
 * Compute RMS voltage and phases of the signals.
 * The MCP3903 ADC registers configuration is explained in the last function.
 *
 * The ADC sampling clock is derived from the TIM2_CH2 clock of 3 MHz:
 *
 *                        MCLK                    AMCLK                      DMCLK
 *     TIM2_CH2 ( 3 MHz ) ----> Prescaller ( /1 ) ----> Clock Divider ( /4 ) ---->
 *
 *     DMCLK                           DRCLK
 *     ----> OSR Clock Divider ( /64 ) ----> ADC sample frquency ( 11718.75 Hz )
 *
 *
 * Used pins:
 *
 *    PB2 - LED WEACT BLUEPILL
 *
 *    PA15 ZERO-CROSS INPUT
 *
 *    PA1 - OSC1 3MHz (TIM2_CH2) ADC
 *    PA2 - USED TO CAPTURE /DRA FROM ADC <- CORRECTION!!! PA0 IS USER KEY ON WEACT, WE USE PA2 !!!
 *
 *    PA8 - HEART BEAT OUTPUT
 *
 *    PB5 - CS SRAM  ( OLD WAS PA3 )
 *    PA4 - SS ADC
 *    PA5 - SCK ADC/SRAM
 *    PA6 - MISO ADC/SRAM
 *    PA7 - MOSI ADC/SRAM
 *
 *    RELAYS
 *    ------
 *    B12  -  K1
 *    B9   -  K2
 *    B13  -  K3
 *    B8   -  K4
 *    B14  -  K5
 *    B7   -  K6
 *    B15  -  K7
 *    B6   -  K8
 *
 *    B0   - SWITCH TO HIGH SIDE DP CHANELS (A,B,C)
 *    B1   - SWITCH TO LOW SIDE DP CHANELS (a,b,c)
 *
 *    If we need to store signed values we use 2's complement
 *    int16_t val = -100;
 *    uint16_t number = (uint16_t) val
 *
 *    The sampling frequency is:
 *        Fs = 11718.75  => we have data ready each 85.33uS
 *
 *    Number of samples is:
 *        num_points = 2048
 *
 *    How many samples we have in one period of 20 ms
 *        Fs / 50 Hz fundamental = 234.375 samples / period of 20 ms
 *
 *    How many periods we have in 2048 samples:
 *        num_points / 234.375 samples per period = 8.7381333 periods
 *
 *    FFT bin frequency:
 *        bin_freq = Fs / nsamples = 5.7220458984375 Hz/bin
 *
 *    Frequency bin near 50 Hz is at index 9
 *        of frequency = bin_freq * 9 FFT bin = 51.4984130859375 Hz
 *
 *    GOERTZEL ALGORITHM (replaces the full FFT)
 *    ===========================================
 *    Instead of computing all 2048 frequency bins (O(N log N) with ~22,000
 *    butterfly operations), Goertzel computes only bin k=9 in O(N) with just
 *    1 multiply + 2 additions per sample.
 *
 *    On Cortex-M3 without FPU this is ~10x faster per channel, and eliminates
 *    the 16 KB xyData[4096] buffer entirely (saves 80% of available SRAM).
 *
 *    The Goertzel recurrence for bin k over N samples:
 *        coeff = 2 * cos(2 * PI * k / N)
 *        s0 = x[n] + coeff * s1 - s2    (for each sample)
 *        s2 = s1; s1 = s0
 *    After all samples:
 *        Re = s1 - s2 * cos(2 * PI * k / N)
 *        Im = s2 * sin(2 * PI * k / N)
 *
 ********************************************************************/

